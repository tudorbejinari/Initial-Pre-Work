// .pop() is a function that allows to remove the last element (number, string, even nested arrays) from an array.
// Example
var ourArray = [1,2,3];
var removedFromOurArray = ourArray.pop();
// removedFromOurArray now equals 3, and ourArray now equals [1,2]

